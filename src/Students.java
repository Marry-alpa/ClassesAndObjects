public class Students {
    String name;
    String surname;
    int age;

    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public String getSurname() {
        return surname;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setSurname(String surname) {
        this.surname = surname;
    }
}
